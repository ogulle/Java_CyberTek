package day18_nestedLoops;

public class NestedLoop {
    public static void main(String[] args) {
        for (int j = 1; j <= 7; j++) {
            int i = 0;

            while (i < 7) {
                System.out.print("* ");
                i++;
            }
            System.out.println();
        }
    }


    }

